package de.rwth.swc.universitymanagement.dto;

public class InstituteRequest {
	private String id;
	private String mail;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getMail() {
		return mail;
	}
	public void setMail(String mail) {
		this.mail = mail;
	}
	

}
